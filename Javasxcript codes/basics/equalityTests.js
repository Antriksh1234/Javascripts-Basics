console.log(3 == 3);    //true
console.log(3 === 3);   //true
console.log(3 == "3");  //true
console.log(3 === "3"); //false
console.log(3 === 4);   //false
console.log(3 === "4"); //false

// == checks content and tries to bring both types to same datatype and then compares
// === is a stricter form of comparision where datatype are also checked